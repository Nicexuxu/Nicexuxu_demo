# 运算符
# 1. + 为合并   字典不可用
print([1, 2] + [3, 2])
# 2. * 为重复   字典不可用
print([1, 2] * 2)
# 3. in 用于判断是否存在，返回布尔值    字典可用，此时检测的是键(key)的值
print(3 in [1,2])
print("s" in {"s": "ss"})
# 4. not in 用于判断是否不存在，返回布尔值  字典可用，此时检测的是键(key)的值
print(3 in [1,2])
# 5. >  >=  == <= <   用于元素比较，返回布尔值
print(1 >= 0)
print("asd" in "1")