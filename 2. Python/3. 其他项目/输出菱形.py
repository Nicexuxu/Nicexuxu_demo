# 复数时

n = int(input("请输入："))
for i in range(1, (n+2)//2):
    print(" "*((n+1)//2-i), end="")     # 末尾数-i，空格越来越少     n为奇数时要多一个空格
    print("*"*(i*2-1))                  #                          n无论奇偶星号都是固定的
for i in range(n//2+1, n+1):            #                          n为奇数时，奇数留给下面的循环，因此多一行
    print(" "*(i-(n//2+1)), end="")     # i-初始数，空格越来越多     n无论奇偶都不用多空格
    print("*"*((n-i)*2+1))              #                          n无论奇偶都没差别